'''
Crie um algoritmo que leia um salário e simule um reajuste positivo de 60%.
Saída esperada:

O salário de 6000 com o reajuste de 60% será de : 9600
'''

salario_base = float(input('Digite seu salario: '))

salario_reajustado = salario_base * 1.6

print(f'O salário de {salario_base} com o reajuste de 60% será de : {salario_reajustado}')